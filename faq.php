<h4>CHIRP Frequently Asked Questions (FAQs) </h4>
<h6><u>General Information</u></h6>
<ol type="1">
    <li><span><b>Q: What is CHIRP? </b></span><br>
        <span><b>A:</b> CHIRP (Chat-bot for Intramural Research Program) is the Large Language Model (LLM) pilot program funded by the Office of Data Science and Strategy and led by the Office of Intramural Research (OIR). It provides NIH staff with a secure platform to explore Generative Artificial Intelligence (GenAI) technologies like LLMs. </span>
    </li>
    <li><span><b>Q: Are there any prerequisites for using CHIRP? </b></span><br>
        <span><b>A:</b> Yes, CHIRP is only accessible to NIH staff and must be used within the secure NIH internal network.</span>
    </li>
    <li><span><b>Q: What makes CHIRP different from other LLM platforms like ChatGPT? </b></span><br>
        <span><b>A:</b> While CHIRP uses the same underlying GPT-4o technology, it is specifically tailored for NIH staff. CHIRP provides a secure, NIH-network-hosted version of the API, ensuring compliance with NIH security policies, which GPT-4o itself does not meet.</span>
    </li>
    <li><span><b>Q: Is CHIRP free or is there a cost? </b></span><br>
        <span><b>A:</b> CHIRP is free for all NIH staff. However, there may be caps on the number of questions or restricted usage during peak times.</span>
    </li>
    <li><span><b>Q: Can CHIRP be used outside the NIH network? </b></span><br>
        <span><b>A:</b> No, CHIRP can only be accessed within the secure NIH internal network.</span>
    </li>
</ol>
<h6><u>Usage and Features</u></h6>
<ol type="1" start="6">
    <li><span><b>Q: How do I get started with CHIRP? </b></span><br>
        <span><b>A:</b> Visit the CHIRP portal at <a href="https://chirp.od.nih.gov/">https://chirp.od.nih.gov/</a> to access the platform. </span>
    </li>
    <li><span><b>Q: What features does CHIRP offer? </b></span><br>
        <span><b>A:</b> CHIRP allows users to ask questions and upload files for analysis by the LLM, providing insights, summaries, and assistance tailored to research needs. .</span>
    </li>
    <li><span><b>Q: Can I customize CHIRP to fit my needs? </b></span><br>
        <span><b>A:</b> Currently, customization options are not available, but future enhancements may be considered based on user feedback.</span>
    </li>
    <li><span><b>Q: What types of files can I upload for analysis? </b></span><br>
        <span><b>A:</b> CHIRP supports formats including .pdf, .json, .docx, .txt, .md, .xml, .png, .jpg, .jpeg, and .gif. The maximum file size for upload is 20 MB.</span>
    </li>
    <li><span><b>Q: I am experiencing an error when trying to upload a .docx file. What could be causing this issue? </b></span><br>
        <span><b>A:</b> If you encounter an error while uploading a .docx file, it may be due to the presence of images within the document. Our current system has difficulty converting Word documents that contain images into text strings. To avoid this issue, you can try uploading a Word document without any images.</span>
    </li>
    <li><span><b>Q: Can CHIRP translate content into other languages? </b></span><br>
        <span><b>A:</b> Yes, CHIRP can assist with translations into various languages, depending on the context and request specifics.</span>
    </li>
    <li><span><b>Q: Can CHIRP integrate with other NIH tools or systems? </b></span><br>
        <span><b>A:</b> Currently, CHIRP operates independently. However, future integrations may be explored based on pilot program outcomes.</span>
    </li>
    <li><span><b>Q: How often is CHIRP updated? </b></span><br>
        <span><b>A:</b> Updates are applied periodically and iteratively to enhance features, security, and accuracy based on user feedback and technical advancements. All updates will be sent to all users via email.</span>
    </li>
    <li><span><b>Q: What browsers are supported by CHIRP? </b></span><br>
        <span><b>A:</b> CHIRP works best on modern browsers, including Google Chrome, Mozilla Firefox, and Microsoft Edge.</span>
    </li>
</ol>
<h6><u>Usage and Features</u></h6>
<ol type="1" start="15">
    <li><span><b>Q: Is my data secure when using CHIRP? </b></span><br>
        <span><b>A:</b> Yes, CHIRP operates within NIH's secure infrastructure, ensuring data protection and confidentiality.</span>
    </li>
    <li><span><b>Q: How does CHIRP handle my personal information? </b></span><br>
        <span><b>A:</b> CHIRP collects limited demographic data, such as the user's Institute/Center (IC), roles, prompts, and responses. This information is used solely for statistical reporting and policy development to improve future implementations.</span>
    </li>
    <li><span><b>Q: Does CHIRP collect Personally Identifiable Information (PII)? </b></span><br>
        <span><b>A:</b> CHIRP utilizes Azure Data Protection software to automatically scrub out PII from processed inputs and uploads, but it is still possible for PII to be temporarily collected if users intentionally or inadvertently share it. To ensure privacy and security, users are strongly advised to avoid entering or uploading any Personally Identifiable Information (PII) when using CHIRP.</span>
    </li>
</ol>
